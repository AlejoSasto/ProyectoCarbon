    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        // TODO add your handling code here:
        int fila = Tbregistro.getSelectedRow();
        if (fila >= 0) {
            txtusu.setText(Tbregistro.getValueAt(fila, 0).toString());
            txtcod.setText(Tbregistro.getValueAt(fila, 1).toString());
            txtcor.setText(Tbregistro.getValueAt(fila, 2).toString());
            txttel.setText(Tbregistro.getValueAt(fila, 3).toString());
            txtdir.setText(Tbregistro.getValueAt(fila, 4).toString());
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione una fila por favor");
        }
    }//GEN-LAST:event_ModificarActionPerformed

