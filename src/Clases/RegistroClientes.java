    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
            PreparedStatement pst = co.prepareStatement("INSERT INTO registro (nombre,cedula,correo,telefono,direccion )VALUES (?,?,?,?,?)");

            pst.setString(1, txtusu.getText());
            pst.setString(2, txtcod.getText());
            pst.setString(3, txtcor.getText());
            pst.setString(4, txttel.getText());
            pst.setString(5, txtdir.getText());

            pst.executeUpdate();
            mostrardatos("");
            
        JOptionPane.showMessageDialog(null, "Registro Guardado");
            
        }catch (Exception e){
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

